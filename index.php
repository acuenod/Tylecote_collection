                - <b>525</b> metallographic mounts from <b>Tylecote's collection</b>. These include notably all the mounts used
                by him in his publication with Brian Gilmour <u>The metallography of early ferrous edge tools and
                edged weapons</u> (1986), samples collected during the expedition lead by T. Wertime in the Near-East,
                samples for the Huelva Archaeo-metallurgical Project and Timna excavations, both led by B. Rothenberg,
                work on copper ingots from Cyprus with M. Balmuth, work on metal object recovered on shipwrecks
                commissioned by the BNFL, and many others.<br>
                <br>
                - <b>250</b> metallographic mounts and a few sectioned objects from <b>H.H. Coghlan's collection</b>. These are
                almost all copper-based and represent most of the objects from the Newbury Museum sampled and 
                examined by Coghlan and published in the last 1950s and the early 1980s. In particular this
                includes many British and Irish Axes and Palstaves and all the samples from Coghlan's publication 
                on the Yattendon hoard (1970).<br>
                <br>
                - <b>50</b> slag mounts from <b>G.R. Morton's collections</b> and in particular the slag examined for his
                publication with Wingrove on Roman slag (1969) and Medieval slag (1972).<br>
                <br>
                It is hoped that more mounts will in time be added to this collection. In particular, it is planned that
                <b>B. Gilmour</b> will shortly add the other half of the samples discussed in Tylecote and Gilmour's publication
                on iron tools and weapons (see above).<br>
