                <h2>History of the collection</h2>
                Ronnie Tylecote is widely considered as one of the fathers of archaeometallurgy. During his
                career he studied an outstanding number of samples from many different horizons, Roman iron
                tools in Britain, Bronze Age copper ingots in Cyprus, slag and ores in Spain and the Near-East to
                cite only a few. He also carried out a number of smelting and casting experiments and analysed
                closely the products he obtained. To add to his own samples, Tylecote also acquired the collections
                of his colleagues H.H. Coghlan and G.R. Morton.<br>
                <br>
                After Tylecote's death in 1990, these collections along with Tylecote's paper archive were donated
                to the HMS. The paper archive is now catalogued and housed at the Ironbridge Gorge Museum Trust,
                while the metallographic collection is subject to a Memorandum of Agreement between the HMS and
                the RLAHA who is housing and managing it since the end of 2012.<br>
                <br>
                In 2013, the collection was catalogued by the RLAHA and this online catalogue was created. It has
                also already helped to support several student's projects and in particular a master's dissertation
                on the re-evaluation of the Yattendon Hoard.<br>
                <br>
