                Although the RLAHA has now catalogued all the mounts of the collection, a number, in particular from
                Tylecote's collection, have not yet been identified and linked back to artefacts. Identification of
                more samples is the subject of ongoing work.<br>
                <br>
                The copper-based samples of the collection are in relatively good condition, often simply showing 
                some tarnish and few scratches. A number of the iron and steel samples on the other hand are corroded
                and the RLAHA is currently working on repolishing the samples and on progressively placing
                them in a metallographic cabinet with controlled humidity. It is hoped that in time most of
                the mounts can be re-prepared and re-imaged to add to the value of this archive as fully
                searchable micro-photographic resource giving a truly unique learning and research tool to
                the archaeometallurgical community.<br>
